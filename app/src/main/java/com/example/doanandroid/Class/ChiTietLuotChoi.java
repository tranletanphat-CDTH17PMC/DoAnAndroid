package com.example.doanandroid.Class;

public class ChiTietLuotChoi {
    private String ID;
    private String LuotChoiID;
    private String CauHoiID;
    private String PhuongAn;
    private int Diem;

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getLuotChoiID() {
        return LuotChoiID;
    }

    public void setLuotChoiID(String luotChoiID) {
        LuotChoiID = luotChoiID;
    }

    public String getCauHoiID() {
        return CauHoiID;
    }

    public void setCauHoiID(String cauHoiID) {
        CauHoiID = cauHoiID;
    }

    public String getPhuongAn() {
        return PhuongAn;
    }

    public void setPhuongAn(String phuongAn) {
        PhuongAn = phuongAn;
    }

    public int getDiem() {
        return Diem;
    }

    public void setDiem(int diem) {
        Diem = diem;
    }
}
