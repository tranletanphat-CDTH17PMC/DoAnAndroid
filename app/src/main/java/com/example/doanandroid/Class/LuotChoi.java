package com.example.doanandroid.Class;

public class LuotChoi {
    private String ID;
    private String NguoiChoiID;
    private String SoCau;
    private String Diem;

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getNguoiChoiID() {
        return NguoiChoiID;
    }

    public void setNguoiChoiID(String nguoiChoiID) {
        NguoiChoiID = nguoiChoiID;
    }

    public String getSoCau() {
        return SoCau;
    }

    public void setSoCau(String soCau) {
        SoCau = soCau;
    }

    public String getDiem() {
        return Diem;
    }

    public void setDiem(String diem) {
        Diem = diem;
    }
}
